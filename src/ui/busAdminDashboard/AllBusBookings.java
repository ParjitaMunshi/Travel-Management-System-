    public void populateTable() {
//        int personId = MySQLUtil.getPersonSession().getId();
        String query = "select * from bus_bookings bb, bus b";
        
        DefaultTableModel model = (DefaultTableModel) tableMyBusBookings.getModel();
        model.setRowCount(0);
        
        try {
            Connection conn = connectMySQL();
            
            PreparedStatement ps = conn.prepareStatement(query);
                        
            ResultSet rs = ps.executeQuery();
            
            while(rs.next()) {
                Object[] row = new Object[13];
                row[0] = rs.getString("bookingId");
                row[1] = rs.getString("busCompanyName");
                row[2] = rs.getString("departingCity");
                row[3] = rs.getString("departingTimeStamp");
                row[4] = rs.getString("departureBusStation");
                row[5] = rs.getString("departureBusTerminal");
                row[6] = rs.getString("arrivalCity");
                row[7] = rs.getString("arrivalTimeStamp");
                row[8] = rs.getString("arrivalBusStation");
                row[9] = rs.getString("arrivalBusTerminal");
                row[10] = rs.getString("seat");
                row[11] = rs.getString("price");
                row[12] = rs.getString("booking_status");
                model.addRow(row);
            }
                        
        } catch (SQLException ex) {
            Logger.getLogger(MySQLUtil.class.getName()).log(Level.SEVERE, "Issue with "
                    + "get all bus query!", ex);
        }
    }

